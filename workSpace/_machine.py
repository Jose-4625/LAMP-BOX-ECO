class Pin:
    def __init__(self, v):
        self.pin = v
class PWM:
    def __init__(self, pin, freq=0, duty=0):
        self.pin = pin
        self.freq = freq
        self.duty = duty
    def freq(x):
        self.freq = x
    def duty(x):
        self.duty = x
    def deinit():
        return 0
